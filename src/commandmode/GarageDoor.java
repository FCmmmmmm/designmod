package commandmode;

public class GarageDoor {
	public void up() {
		System.out.println("door-up");
	}
	
	public void down() {
		System.out.println("door-down");
	}
	
	public void stop() {
		System.out.println("door-stop");
	}
	
	public void lightOn() {
		System.out.println("light-on");
	}
	
	public void lightoff() {
		System.out.println("light-off");
	}
}
